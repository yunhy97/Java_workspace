package com.sample.school.vo;

public class Department {
//학과: 번호, 학과명
	
	private int no;
	private String deptName;
	
	public Department() {}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getDeptName() {
		return deptName;
	}

	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	
	
}
